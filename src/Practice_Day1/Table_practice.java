package Practice_Day1;
import java.util.Scanner; 
public class Table_practice {

	public static void main(String args[])   
	{  
	Scanner sc = new Scanner(System.in);  
	System.out.print("Enter number: ");       
	int s=sc.nextInt();  
	for(int i=1; i <= 10; i++)  
	{  
	System.out.println(s+" * "+i+" = "+s*i);  
	  
	}  
	}  
	
	
}
